function calcSum(a, b) {
    return a + b;
}
function calcSub(a, b) {
    return a - b;
}
function calcMulti(a, b) {
    return a * b;
}
function calcDiv(a, b) {
    return a / b;
}

module.exports.calcSum = calcSum;
module.exports.calcSub = calcSub;
module.exports.calcMulti = calcMulti;
module.exports.calcDiv = calcDiv;